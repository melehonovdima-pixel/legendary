require_executor = require_role([UserRole.ADMIN, UserRole.MANAGER, UserRole.EXECUTOR])
require_client = require_role([UserRole.ADMIN, UserRole.MANAGER, UserRole.CLIENT, UserRole.EXECUTOR])
